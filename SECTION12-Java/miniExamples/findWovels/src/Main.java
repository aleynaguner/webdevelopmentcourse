public class Main {

    public static void main(String[] args) {
        char letter = 's';

        switch (letter){
            case 'A':
            case 'E':
            case 'I':
            case 'U':
            case 'O':
                System.out.println("Vowel char");
                break;
            default:
                System.out.println("Not a vowel char");

        }
    }
}
